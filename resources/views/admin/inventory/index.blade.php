<script>
$(document).ready(function() {
    $('#inventory_count_new').click(function(e) {
        e.preventDefault();
        $('.ui-draggable-create-inventory-count').show();
        $('.ui-widget-overlay').css('display', 'block');
    });

    $('.closethick-inventory-close').click(function() {
        $('.ui-draggable-create-inventory-count').hide();
        $('.ui-widget-overlay').css('display', 'none');
    });

    $('#frm_new_inv_count').validate({
        rules: {
            'new_inv_count_date': 'required',
        },
        messages: {
            'new_inv_count_date': 'Please select a inventory count date.',
        }
    })

    $('#frm_new_inv_count').submit(function(e) {
        e.preventDefault();
        if (!$(this).find('input.error').length) {
            var formData = $('#frm_new_inv_count').serialize();
            console.log(formData);
            $.ajax({
                url: "{{route('admin.inventory-count.store')}}",
                type: 'POST',
                data: formData,
                success: function(response) {
                    if (response.success) {
                        // window.location.reload(true);
                        var id = response.last_id;
                        console.log(id);
                        var url = "{{ route('admin.inventory-count.show', ':id') }}"
                            .replace(':id', id);
                        window.location.href = url;
                    } else {
                        alert(response.message);
                        location.reload();
                    }
                },
                error: function(xhr, status, error) {
                    // Handle errors
                    console.error(error);
                }
            });
        }
    });

    $(document).on('click', '.btn_delete_invcunt', function() {
        $.ajaxSetup({
            headers: {
                'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
            }
        });
        id = $(this).attr('data-id');

        if (confirm("Do you really want to delete this record?") == true) {
            var url = "{{ route('admin.inventory-count.destroy', ':id') }}";
            url = url.replace(':id', id);
            $.ajax({
                url: url
                , type: 'DELETE'
                , success: function(response) {
                    if (response.success) {
                        alert(response.message);
                        window.location.href = "{{ route('admin.inventory-count.index') }}";
                    } else {
                        alert(response.message);
                    }
                }
            });

        }
    });
});
</script>
